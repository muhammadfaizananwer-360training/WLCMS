package com.softech.ls360.lcms.contentbuilder.model;

public class SlideTemplate {
	
	private int slideTemplateID;
	
	private String slideTemplateName;
	
	private String slideTemplateURL;

	public int getSlideTemplateID() {
		return slideTemplateID;
	}

	public void setSlideTemplateID(int slideTemplateID) {
		this.slideTemplateID = slideTemplateID;
	}

	public String getSlideTemplateName() {
		return slideTemplateName;
	}

	public void setSlideTemplateName(String slideTemplateName) {
		this.slideTemplateName = slideTemplateName;
	}

	public String getSlideTemplateURL() {
		return slideTemplateURL;
	}

	public void setSlideTemplateURL(String slideTemplateURL) {
		this.slideTemplateURL = slideTemplateURL;
	} 

}
