package com.softech.ls360.lcms.contentbuilder.web.vo;

public class OfferEmailVO {
	private String industry;
	private String authorEmail;
	private String courseName;
	private String courseId;
	private String authorName;
	private String toContentOwnerEmail;
	
	public String getIndustry() {
		return industry;
	}
	public void setIndustry(String industry) {
		this.industry = industry;
	}
	public String getAuthorEmail() {
		return authorEmail;
	}
	public void setAuthorEmail(String authorEmail) {
		this.authorEmail = authorEmail;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getCourseId() {
		return courseId;
	}
	public void setCourseId(String courseId) {
		this.courseId = courseId;
	}
	public String getAuthorName() {
		return authorName;
	}
	public void setAuthorName(String authorName) {
		this.authorName = authorName;
	}
	public String getToContentOwnerEmail() {
		return toContentOwnerEmail;
	}
	public void setToContentOwnerEmail(String toContentOwnerEmail) {
		this.toContentOwnerEmail = toContentOwnerEmail;
	}
	
	
}
